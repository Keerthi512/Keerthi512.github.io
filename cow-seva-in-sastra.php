									<h3 style="margin: 0 0 10px 0;">Cows in Rik Samhita</h3>
									<p>यः पौरुषेण क्रविषा समंक्ते यो अश्वेन पशुना यातुधानः ।</p>
									<p>ये अघ्न्याये भरति क्षीरमग्ने तेषां शीर्षाणि हरसापि वृश्चः ॥</p>
									<p>(Rik Samhita 87 – 161)</p>
									<p>Oh fire god, with your flames burn the heads of those demons who eat the meat of humans, animals like horse and cow, and those who steal cows’ milk.</p>
									<p>प्रजापतिर्मह्यमेता रराणो विश्वैर्देवैः पितृभिः संविदानः ।</p>
									<p>शिवाः सतीरुप नो गोष्ठमाकस्तासां वयं प्रजया संसदेम ॥</p>
									<p>(Rik Samhita 10 – 169 – 4)</p>
									<p>May the supreme Lord, complemented by all the Gods, create auspicious and spacious cowsheds for our happiness and populate them with cows and calves. Let us rejoice the cow-wealth and contend by serving those cows.</p>
									<h3>Cows in Padmapurana</h3>
									<p>गावो बंधुर्मनुष्याणां मनुष्याबांधवा गवाम् ।</p>
									<p>गौः यस्मिन् गृहेनास्ति तद्बंधुरहितं गृहम् ॥</p>
									<p>(Padmapurana)</p>
									<p>Cows are the abode of the Goddess of wealth. Sins don’t touch them. There exists a fine relationship between man and cow. A home without a cow is like one without dear ones.</p>
									<h3>Cows in Mahabharata, Anushasana</h3>
									<p>गोकुलस्य तृषार्तस्य जलार्थे वसुधाधिपः ।</p>
									<p>उत्पादयति यो विघ्नं तं विद्याद्ब्रह्मघातिनम् ॥</p>
									<p>(Mahabharata, Anushasana Parva 24-7)</p>
									<p>Obstructing provision of water to thirsty cows should be considered equal to the sin of killing Brahmins.</p>
									<p>गवां मूत्रपुरीषस्य नोद्विजेत कथंचन ।</p>
									<p>न चासां मांसमश्नीयाद्गवां पुष्टिं तथाप्नुयात् ॥</p>
									<p>(Mahabharata, Anushasana Parva 78-17)</p>
									<p>Do not hesitate to consume cow urine and cow dung – they are sacred. But one should never eat the cow meat. A person becomes stronger by consuming Panchagavya.</p>
									<p>गावो ममाग्रतो नित्यं गावः पृष्ठत एव च ।</p>
									<p>गावो मे सर्वतश्चैव गवां मध्ये वसाह्यहम् ॥</p>
									<p>(Mahabharata, Anushasana Parva 80-3)</p>
									<p>Let there be cows in front of me, behind me and all around me. I live with the cows.</p>
									<p>दानानामपि सर्वेषां गवां दानं प्रशस्यते ।</p>
									<p>गावः श्रेष्ठाः पवित्राश्च पावनं ह्येतदुत्तमम् ॥</p>
									<p>(Mahabharata, Anushasana Parva 83-3)</p>
									<p>Donation of cows is superior to all others. Cows are supreme and sacred.</p>
									<p>सर्वोपनिषदो गावो दोग्धा गोपालनंदनः ।</p>
									<p>पार्थो वत्सः सुधीर्भोक्तादुग्धं गीतामृतः महत् ॥</p>
									<p>Bhagavad-Gita is the essence of Upanishads. It is like a cow whom Srikrishna milks. Arjuna is like a calf. The learned devotees are drinking the ambrosial milk of Bhagavad-Gita.</p>
									<p>गौर्मे माता वृषभः पिता मे दिवं शर्म जगते मे प्रतिष्ठा ।</p>
									<p>Cow is my mother and ox my father. Let the pair bless me with happiness in this world and bliss in heaven. I depend on cow for my life – thus stating one should surrender to cow.</p>
